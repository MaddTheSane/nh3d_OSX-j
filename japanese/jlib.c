int
isspace_8(c)
     int c;
{
    unsigned int *up;

    up = (unsigned int *)&c;
    return *up<0x80 ? isspace(*up) : 0;
}
/*
** split string(str) including japanese before pos and return to
** str1, str2.
*/
void
split_japanese( str, str1, str2, pos )
     char *str;
     char *str1;
     char *str2;
     int pos;
{
    int len, i, j, k, mlen;
    char *pstr;
    char *pnstr;
    int	sq_brac;

retry:
    len = strlen((char *)str);

    if( len < pos ){
	strcpy(str1,str);
	*str2 = '\0';
	return;
    }

    if(pos > 20)
      mlen = 20;
    else
      mlen = pos;

    i = pos;
    if(is_kanji2(str, i))
      --i;

/* 1:
** search space character
*/
    j = 0;
    while( j<mlen ){
	if(isspace_8(str[i-j])){
	    --j;
	    goto found;
	}
	else if(is_kanji1(str,i-j)){
	    if(!strncmp(str+i-j,"¡¡",2)){
		j -= 2;
		goto found;
	    }
	}
	++j;
    }

/* 3:
** search second bytes of japanese
*/
    j = 0;
    sq_brac = 0;
    while( j<mlen ){
	if(str[i-j] == '['){
	    sq_brac = 0;
	    goto found;
	}
	else if(str[i-j] == ']')
	  sq_brac = 1;
	
	if(!sq_brac && is_kanji1(str,i-j)){
	    goto found;
	}

	++j;
    }
    if(pos > 2){
	pos -= 2;
	goto retry;
    }
found:

    if(pos > 2){
	if((str[i-j] == ']' ||
	    str[i-j] == ')' ||
	    str[i-j] == '}')){
	    pos -= 2;
	    goto retry;
	}
	else if(!strncmp(str+i-j, "¡Ï", 2) ||
		!strncmp(str+i-j, "¡Ë", 2) ||
		!strncmp(str+i-j, "¡Ñ", 2)){
	    pos -= 2;
	    goto retry;
	}
	else if(!strncmp(str+i-j,"¡©",2) ||
		!strncmp(str+i-j,"¡¢",2) ||
		!strncmp(str+i-j,"¡£",2) ||
		!strncmp(str+i-j,"¡¤",2) ||
		!strncmp(str+i-j,"¡¥",2)){
	    pos -= 2;
	    goto retry;
	}
    }

    if(i == j){
	/* Ê¬³äÅÀ¤¬¤Ê¤«¤Ã¤¿ */
	strcpy(str1, str);
	str2[0] = '\0';
	return;
    }

    pstr = str;

    pnstr = str1;
    for( k=0 ; k<i-j ; ++k )
      *(pnstr++) = *(pstr++);
    *(pnstr++) = '\0';

    pnstr = str2;
    for( ; str[k] ; ++k )
      *(pnstr++) = *(pstr++);
    *(pnstr++) = '\0';
}

void 
jrndm_replace(c)
     char *c;
{
    unsigned char cc[3];

    if(IC==SJIS)
      memcpy(cc, (char *)sj2e(c), 2);
    else
      memcpy(cc, c, 2);

    cc[0] &= 0x7f;
    cc[1] &= 0x7f;

    switch(cc[0]){
      case 0x21:
	cc[1] = rn2(94) + 0x21;
	break;
      case 0x23:
	if(cc[1] <= 0x39) /* £°¡Á£¹ */
	  cc[1] = rn2(10) + 0x30;
	else if(cc[1] <= 0x5A) /* £Á¡Á£Ú */
	  cc[1] = rn2(26) + 0x41;
	else if(cc[2] <= 0x7A) /* £á¡Á£ú */
	  cc[1] = rn2(26) + 0x61;
	break;
      case 0x24:
      case 0x25:
	cc[1] = rn2(83) + 0x21; /* ¤¢¡Á¤ó or ¥¢¡Á¥ó */
	break;
      case 0x26:
	if(cc[1] <= 0x30)
	  cc[1] = rn2(24) + 0x21; /* ¦¡¡Á¦¸ ¥®¥ê¥·¥ãÊ¸»ú */
	else
	  cc[1] = rn2(24) + 0x41; /* ¦Á¡Á¦Ø ¥®¥ê¥·¥ãÊ¸»ú */
	break;
      case 0x27:
	if(cc[1] <= 0x40)
	  cc[1] = rn2(33) + 0x21; /* §¡¡Á§Á ¥í¥·¥¢Ê¸»ú */
	else
	  cc[1] = rn2(33) + 0x51; /* §Ñ¡Á§ñ ¥í¥·¥¢Ê¸»ú */
	break;
      case 0x4f:
	cc[1] = rn2(51) + 0x21; /* Ï¡¡Á ÏÓ */
	break;
      case 0x74:
	cc[1] = rn2(4) + 0x21; /* ô¡ ô¢ ô£ ô¤ ¤Î4Ê¸»ú*/
	break;
      default:
	if(cc[0] >= 0x30 && cc[1] <= 0x74)
	  cc[1] = rn2(94) + 0x21;
	break;
    }

    cc[0] |= 0x80;
    cc[1] |= 0x80;

    if(IC==SJIS)
      memcpy(c, (char *)e2sj(cc), 2);
    else
      memcpy(c, cc, 2);
}

